package com.uyr.yusara.dreamhome.Modal;

public class Posts
{
    public String uid;
    public String time;
    public String date;
    public String description;
    public String postImage;
    public String productname;
    public String price;
    public String propertytype;
    public String bathroom;
    public String bedrooms;
    public String size;
    public String firmtype;
    public String firmnumber;
    public String description2;

    public Posts()
    {

    }

    public Posts(String uid, String time, String date, String description, String postImage, String productname, String price, String propertytype, String bathroom, String bedrooms, String size, String firmtype, String firmnumber, String description2) {
        this.uid = uid;
        this.time = time;
        this.date = date;
        this.description = description;
        this.postImage = postImage;
        this.productname = productname;
        this.price = price;
        this.propertytype = propertytype;
        this.bathroom = bathroom;
        this.bedrooms = bedrooms;
        this.size = size;
        this.firmtype = firmtype;
        this.firmnumber = firmnumber;
        this.description2 = description2;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPropertytype() {
        return propertytype;
    }

    public void setPropertytype(String propertytype) {
        this.propertytype = propertytype;
    }

    public String getBathroom() {
        return bathroom;
    }

    public void setBathroom(String bathroom) {
        this.bathroom = bathroom;
    }

    public String getBedrooms() {
        return bedrooms;
    }

    public void setBedrooms(String bedrooms) {
        this.bedrooms = bedrooms;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getFirmtype() {
        return firmtype;
    }

    public void setFirmtype(String firmtype) {
        this.firmtype = firmtype;
    }

    public String getFirmnumber() {
        return firmnumber;
    }

    public void setFirmnumber(String firmnumber) {
        this.firmnumber = firmnumber;
    }

    public String getDescription2() {
        return description2;
    }

    public void setDescription2(String description2) {
        this.description2 = description2;
    }

    public String getProductname() {
        return productname;
    }

    public void setProductname(String productname) {
        this.productname = productname;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPostImage() {
        return postImage;
    }

    public void setPostImage(String postImage) {
        this.postImage = postImage;
    }
}
